public class VariaveisJava {

	public static void main(String[] args) {

		System.out.println("Variáveis Java");
		System.out.println("-------------------------------------------------------------");
		System.out.println("Exemplo: Crie uma variável chamada nome do tipo String e atribua a ela o valor \"Augusta\": \n");
		System.out.println("String nome = \"Augusta\";");
		System.out.println("System.out.println(nome);\n");
		System.out.println("-------------------------------------------------------------");

		String nome = "Augusta";
		System.out.println(nome);
	}
}
