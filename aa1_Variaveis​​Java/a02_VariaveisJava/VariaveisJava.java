public class VariaveisJava {

	public static void main(String[] args) {

		System.out.println("Variáveis Java");
		System.out.println("-------------------------------------------------------------");
		System.out.println("Exemplo: Crie uma variável que armaze um numero, chamada numero do tipo int  e atribua a ela o valor 49: \n");
		System.out.println("int numero = 49;");
		System.out.println("System.out.println(numero);\n");
		System.out.println("-------------------------------------------------------------");

		int numero = 49;
		System.out.println(numero);
	}
}
