public class ImprimirCalculoMatematico {

	public static void main(String[] args) {

		System.out.println("Você também pode realizar cálculos matemáticos dentro do println() método.");
		System.out.println(9 + 7);
		System.out.println(6 - 4);
		System.out.println(3 * 5);
		System.out.println(8 / 2);
	}
}
